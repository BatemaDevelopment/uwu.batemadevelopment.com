function uwu() {
  let randint = Math.round(Math.random() * 3);

  if (randint === 0) {
    window.location.replace('https://www.batemadevelopment.com');
  } else if (randint === 1) {
    window.location.replace('https://dev.webalina.batemadevelopment.com');
  } else if (randint === 2) {
    window.location.replace('https://blinking.terminal.batemadevelopment.com');
  }
}